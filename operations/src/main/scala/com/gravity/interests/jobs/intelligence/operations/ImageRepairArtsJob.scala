/**
 * Emit the ArticleKeys and CampaignArticleKeys of images that need fixing.
 */
class ImageRepairArtsJob(outputDirectory: String, combineOutput: Boolean = true) extends Serializable {
  import ImageRepairArtsJob._
  import grvcascading._

  val fldNames = List("scopedKey", "imgMd5")

  // An image needs fixing if it is an S3/CDN Image URL whose embedded MD5 appears in the badMd5s list.
  def imageNeedsFixing(imgStr: String) = {
    ParsedCachedImageUrl.tryFromUrl(imgStr) match {
      case Some(parsedCdnUrl) => badMd5s.contains(parsedCdnUrl.md5HashKey)

      case None => false
    }
  }

  def optImageNeedsFixing(optImgStr: Option[String]) =
    optImgStr.map(imageNeedsFixing).getOrElse(false)

  def casNeedsFixing(cas: CampaignArticleSettings) =
    optImageNeedsFixing(cas.image)

  val counterGroup = "Custom - ImageRepairArtsJob"

  val source       = grvcascading.fromTable(Schema.Articles)(_.withColumn(_.image).withFamilies(_.campaignSettings))
  val destination  = grvcascading.toTextDelimited(outputDirectory)(fldNames: _*)

  val assembly = grvcascading.pipe("Map Articles in ImageRepairArtsJob").each((flowProcess, call) => {
    flowProcess.increment(counterGroup, "ArticleRows Read", 1)

    val article = call.getRow(Schema.Articles)

    if (imageNeedsFixing(article.image)) {
      flowProcess.increment(counterGroup, "Article Image Needs Repair", 1)
      call.write(article.articleKey.toScopedKey, ImageCachingService.effectiveMd5HashKey(ImageUrl(article.image)).hexstr)
    }

    for {
      (ck , cas) <- article.campaignSettings
      if casNeedsFixing(cas)
    } {
      flowProcess.increment(counterGroup, "CampaignArticlesSettings Image Needs Repair", 1)
      call.write(CampaignArticleKey(ck, article.articleKey).toScopedKey, ImageCachingService.effectiveMd5HashKey(ImageUrl(cas.image.get)).hexstr)
    }
  })(fldNames: _*)

  grvcascading.flow.connect("ImageRepairArtsJob Main Flow", source, destination, assembly).complete()

  // If so requested, combine the output of the run to a single file.
  if (combineOutput) {
    val outputHdfsFile = outputDirectory + ".tsv"

    try {
      DeprecatedHBase.delete(HBaseConfProvider.getConf.fs, new Path(outputHdfsFile))
    } catch {
      case ex: Exception =>
    }

    FileUtil.copyMerge(HBaseConfProvider.getConf.fs, new Path(outputDirectory), HBaseConfProvider.getConf.fs,
      new Path(outputHdfsFile), false, HBaseConfProvider.getConf.defaultConf, null)
  }
}

